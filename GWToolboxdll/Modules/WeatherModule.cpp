void WeatherModule::DrawInWorld(IDirect3DDevice9* device)
{
    if (!textures_requested) {
        raindrop_tex_pp = GwDatTextureModule::LoadTextureFromFileId(kRaindropFileId);
        snowflake_tex_pp = GwDatTextureModule::LoadTextureFromFileId(kSnowflakeFileId);
        splash_tex_pp = GwDatTextureModule::LoadTextureFromFileId(kSplashFileId);
        textures_requested = true;
    }

    // Throttle the physics + geometry rebuild to ~30 Hz; the cached vertex buffers are still drawn every
    // frame below, so motion advances in 30 Hz steps (with the full elapsed dt) while rendering stays smooth.
    if (!last_update || TIMER_DIFF(last_update) >= kUpdateIntervalMs) {
        const float dt = last_update ? std::clamp(static_cast<float>(TIMER_DIFF(last_update)) / 1000.f, 0.f, 0.1f) : 0.f;
        last_update = TIMER_INIT();
        SyncWeather(device, GW::CameraMgr::GetCamera(), dt);
    }

    DrawAmbient(device, ambient_strength); // dim the scene first, so the particles stay bright on top

    IDirect3DTexture9* rain_tex = raindrop_tex_pp ? *raindrop_tex_pp : nullptr;
    IDirect3DTexture9* snow_tex = snowflake_tex_pp ? *snowflake_tex_pp : nullptr;
    IDirect3DTexture9* splash_tex = splash_tex_pp ? *splash_tex_pp : nullptr;
    const bool have_rain = rain_ready && rain_tex && !rain_instances.empty();
    const bool have_snow = snow_ready && snow_tex && !snow_vertices.empty();
    const bool have_splash = splash_ready && splash_tex && !splash_vertices.empty();
    if ((!have_rain && !have_snow && !have_splash) || !EnsureShaders(device)) return;

    IDirect3DStateBlock9* state_block = nullptr; // restored on exit so GW's own rendering isn't corrupted
    if (device->CreateStateBlock(D3DSBT_ALL, &state_block) != D3D_OK) return;
    if (device->SetPixelShader(weather_ps) == D3D_OK && GameWorldCompositor::SetWorldViewProj(device, kZNear, kZFar)) {
        GameWorldCompositor::SetWorldRenderStates(device, occlude_behind_terrain);
        GameWorldCompositor::SetDistanceFog(device, render_max_distance, fog_factor);
        device->SetSamplerState(0, D3DSAMP_MINFILTER, D3DTEXF_LINEAR);
        device->SetSamplerState(0, D3DSAMP_MAGFILTER, D3DTEXF_LINEAR);
        device->SetSamplerState(0, D3DSAMP_ADDRESSU, D3DTADDRESS_CLAMP);
        device->SetSamplerState(0, D3DSAMP_ADDRESSV, D3DTADDRESS_CLAMP);
        // One shared index buffer serves both passes: the instanced pass only needs its first quad's 6 indices.
        const size_t indexed_verts = std::max(snow_vertices.size(), splash_vertices.size());
        const bool ib_ok = EnsureQuadIB(device, std::max<size_t>(indexed_verts / 4, 1)) && device->SetIndices(quad_ib) == D3D_OK;

        // Indexed pass: snow flakes + settle (snow_tex) and splashes (splash_tex), CPU-built 4-vert quads.
        if (ib_ok && device->SetVertexShader(weather_vs) == D3D_OK && device->SetVertexDeclaration(weather_decl) == D3D_OK) {
            const auto draw = [&](IDirect3DVertexBuffer9* vb, const size_t nverts) {
                if (device->SetStreamSource(0, vb, 0, sizeof(WeatherVertex)) != D3D_OK) return;
                device->DrawIndexedPrimitive(D3DPT_TRIANGLELIST, 0, 0, static_cast<UINT>(nverts), 0, static_cast<UINT>(nverts / 2));
            };
            if (have_snow) {
                device->SetTexture(0, snow_tex);
                draw(snow_vb, snow_vertices.size());
            }
            if (have_splash) {
                device->SetTexture(0, splash_tex);
                draw(splash_vb, splash_vertices.size());
            }
        }

        // Instanced pass: each rain drop is one record expanded into a quad on the GPU (stream 0 = unit
        // quad, stream 1 = per-drop data with INSTANCEDATA frequency).
        if (ib_ok && have_rain && device->SetVertexShader(weather_inst_vs) == D3D_OK && device->SetVertexDeclaration(weather_inst_decl) == D3D_OK) {
            const auto tint = ImGui::ColorConvertU32ToFloat4(rain_color);
            const float tintf[4] = {tint.x, tint.y, tint.z, tint.w};
            const float flags[4] = {1.f, 0.f, 0.f, 0.f}; // flip texture V (streak runs along the downward velocity)
            device->SetVertexShaderConstantF(8, tintf, 1);
            device->SetVertexShaderConstantF(9, flags, 1);
            const UINT ninst = static_cast<UINT>(rain_instances.size());
            if (device->SetStreamSource(0, quad_geom_vb, 0, sizeof(GeomVert)) == D3D_OK && device->SetStreamSourceFreq(0, D3DSTREAMSOURCE_INDEXEDDATA | ninst) == D3D_OK && device->SetStreamSource(1, rain_inst_vb, 0, sizeof(WeatherInstance)) == D3D_OK &&
                device->SetStreamSourceFreq(1, D3DSTREAMSOURCE_INSTANCEDATA | 1u) == D3D_OK) {
                device->SetTexture(0, rain_tex);
                device->DrawIndexedPrimitive(D3DPT_TRIANGLELIST, 0, 0, 4, 0, 2);
            }
            device->SetStreamSourceFreq(0, 1); // restore non-instanced frequency (state block also restores)
            device->SetStreamSourceFreq(1, 1);
        }
    }
    state_block->Apply();
    state_block->Release();
}

void WeatherModule::RegisterSettings(ToolboxModule* module)
{
    SettingsRegistry::RegisterField(module, "occlude_behind_terrain", &occlude_behind_terrain);
    SettingsRegistry::RegisterField(module, "render_max_distance", &render_max_distance);
    SettingsRegistry::RegisterField(module, "rain_color", &rain_color);
    SettingsRegistry::RegisterField(module, "ambient_color", &ambient_color);
}

void WeatherModule::OnSettingsLoaded()
{
    render_max_distance = std::clamp(render_max_distance, 10.0f, kMaxRadius);
    for (auto& c : conditions) {
        c.type = std::clamp(c.type, 0, kTypeCount - 1);
        c.drop_count = std::clamp(c.drop_count, 0, 20000);
        c.spread_radius = kMaxRadius; // fixed, not user-editable
        c.splash_chance = std::clamp(c.splash_chance, 0.f, 1.f);
        c.sound_min_interval = std::max(c.sound_min_interval, 0.f);
        c.sound_max_interval = std::max(c.sound_max_interval, c.sound_min_interval);
        c.ambient = std::clamp(c.ambient, 0.f, 1.f);
    }
}

void WeatherModule::LoadSettings(SettingsDoc& doc, ToolboxIni* legacy)
{
    ToolboxModule::LoadSettings(doc, legacy);
    doc.Get(Name(), "conditions", conditions); // keeps defaults if the key is absent
    OnSettingsLoaded();
}

void WeatherModule::SaveSettings(SettingsDoc& doc)
{
    ToolboxModule::SaveSettings(doc);
    doc.Set(Name(), "conditions", conditions);
}

void WeatherModule::DrawSettings()
{
    const auto red = ImGui::ColorConvertU32ToFloat4(Colors::Red());
    const auto green = ImGui::ColorConvertU32ToFloat4(Colors::Green());
    ImGui::TextColored(red, "Warning: This is a beta feature.");
    if (!GameWorldCompositor::IsActive()) ImGui::TextColored(red, GameWorldCompositor::HasFailed() ? "  in-world compositor FAILED to install." : "  in-world compositor: not installed yet.");

    ImGui::Checkbox("Occlude behind terrain", &occlude_behind_terrain);
    ImGui::DragFloat("Maximum render distance", &render_max_distance, 5.f, 10.f, kMaxRadius, "%.0f", ImGuiSliderFlags_AlwaysClamp);
    auto col = ImGui::ColorConvertU32ToFloat4(rain_color);
    if (ImGui::ColorEdit4("Tint", &col.x, ImGuiColorEditFlags_AlphaBar)) rain_color = ImGui::ColorConvertFloat4ToU32(col);
    auto amb = ImGui::ColorConvertU32ToFloat4(ambient_color);
    if (ImGui::ColorEdit3("Overcast tint", &amb.x)) ambient_color = ImGui::ColorConvertFloat4ToU32(amb);
    ImGui::ShowHelp("Colour the scene is tinted toward while an overcast condition is active.\nPer-condition strength is set with each condition's \"Overcast\" slider.");

    ImGui::Separator();
    int to_remove = -1;
    for (int i = 0; i < static_cast<int>(conditions.size()); i++) {
        auto& c = conditions[i];
        ImGui::PushID(i);
        ImGui::Checkbox("##active", &c.active);
        ImGui::SameLine();
        if (ImGui::CollapsingHeader(c.name.empty() ? "(unnamed)" : c.name.c_str())) {
            ImGui::InputText("Name", c.name, 32);
            const char* type_names[kTypeCount] = {"Rain", "Snow"};
            ImGui::Combo("Type", &c.type, type_names, kTypeCount);
            ImGui::DragInt("Drop count", &c.drop_count, 25.f, 0, 20000, "%d", ImGuiSliderFlags_AlwaysClamp);
            ImGui::DragFloat("Drop size", &c.drop_size, 1.f, 1.f, 500.f, "%.0f");
            ImGui::DragFloat("Fall speed", &c.fall_speed, 50.f, 0.f, 30000.f, "%.0f");
            ImGui::DragFloat2("Wind (x, y)", &c.wind_x, 10.f, -10000.f, 10000.f, "%.0f");
            if (HasSplash(c.type)) ImGui::DragFloat("Splash chance", &c.splash_chance, 0.01f, 0.f, 1.f, "%.2f", ImGuiSliderFlags_AlwaysClamp);
            ImGui::DragFloat("Overcast", &c.ambient, 0.01f, 0.f, 1.f, "%.2f", ImGuiSliderFlags_AlwaysClamp);
            ImGui::ShowHelp("How strongly this condition dims the scene toward the overcast tint while active.");

            ImGui::TextUnformatted("Sounds (file id, played at random while active)");
            int snd_remove = -1;
            for (int s = 0; s < static_cast<int>(c.sounds.size()); s++) {
                ImGui::PushID(s);
                ImGui::SetNextItemWidth(120.f);
                ImGui::InputScalar("##sid", ImGuiDataType_U32, &c.sounds[s], nullptr, nullptr, "%X", ImGuiInputTextFlags_CharsHexadecimal);
                const bool ok = IsValidSound(c.sounds[s]);
                ImGui::SameLine();
                ImGui::TextColored(ok ? green : red, ok ? ICON_FA_CHECK : ICON_FA_TIMES);
                ImGui::SameLine();
                if (ImGui::SmallButton("Test")) AudioSettings::PlaySoundFileId(c.sounds[s]);
                ImGui::SameLine();
                if (ImGui::SmallButton("Remove##snd")) snd_remove = s;
                ImGui::PopID();
            }
            if (snd_remove >= 0) c.sounds.erase(c.sounds.begin() + snd_remove);
            if (ImGui::SmallButton("Add sound")) c.sounds.push_back(0);
            ImGui::DragFloat2("Sound interval (s)", &c.sound_min_interval, 0.5f, 0.f, 600.f, "%.0f");
            ImGui::Checkbox("Play sounds in 3D", &c.sound_3d);
            ImGui::ShowHelp("Play each sound from a random nearby position so the game's 3D audio varies its\nvolume and stereo panning - reduces repetition when there's only one sound.\nNo effect if the sound isn't a positional one.");

            if (ImGui::Button("Remove condition")) to_remove = i;
        }
        ImGui::PopID();
    }
    if (to_remove >= 0) {
        conditions.erase(conditions.begin() + to_remove);
        if (to_remove < static_cast<int>(particles.size())) particles.erase(particles.begin() + to_remove);
    }

    if (ImGui::Button("Add condition")) conditions.push_back({});
    ImGui::SameLine();
    if (ImGui::Button("Reset to defaults")) ImGui::OpenPopup("Reset weather conditions?");
    if (ImGui::BeginPopupModal("Reset weather conditions?", nullptr, ImGuiWindowFlags_AlwaysAutoResize)) {
        ImGui::Text("Replace all conditions with the %d defaults?", static_cast<int>(DefaultConditions().size()));
        if (ImGui::Button("Reset")) {
            conditions = DefaultConditions();
            particles.clear();
            ImGui::CloseCurrentPopup();
        }
        ImGui::SameLine();
        if (ImGui::Button("Cancel")) ImGui::CloseCurrentPopup();
        ImGui::EndPopup();
    }
}

