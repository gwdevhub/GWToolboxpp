void DeathPenaltyTimer::Update(float delta)
{
    ToolboxUIPlugin::Update(delta);

    const auto player = GW::Agents::GetControlledCharacter();
    if (!player) return;
    const auto currentlyDead = !player->GetIsAlive();

    if (wasPreviouslyDead && !currentlyDead) 
    {
        lastRevive = std::chrono::steady_clock::now();
    }
    wasPreviouslyDead = currentlyDead;
}

void DeathPenaltyTimer::Draw(IDirect3DDevice9* pDevice)
{
    if (!GetVisiblePtr() || !*GetVisiblePtr())
        return;

    const auto msSinceRevive = std::chrono::duration_cast<std::chrono::milliseconds>(std::chrono::steady_clock::now() - lastRevive).count();
    if (msSinceRevive > 15'000 || GW::Map::GetInstanceType() == GW::Constants::InstanceType::Loading) return;
    if (!texture && !textureLoadingFailed) loadTexture(pDevice);

    const auto thickness = radius / 5;
    ImGui::SetNextWindowSize(ImVec2(2 * radius + thickness, 2 * radius + thickness));
    SetNextWindowCenter(ImGuiCond_FirstUseEver);
    if (ImGui::Begin(Name(), nullptr, GetWinFlags() | ImGuiWindowFlags_NoBackground)) 
    {
        const auto circlePortion = (float)msSinceRevive / 15'000;
        drawCircleSegment(circlePortion, thickness);
    }
    ImGui::End();
}

